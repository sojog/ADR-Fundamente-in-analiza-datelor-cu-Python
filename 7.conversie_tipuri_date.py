
## String
x = "10"
print(x, type(x))

## String -> INT
x_int = int(x)
print(x_int, type(x_int))

## String -> FLOAT
x_float = float(x)
print(x_float, type(x_float))

## String -> BOOL
x_bool = bool(x)
print(x_bool, type(x_bool))


## INT
y = 33
print(y, type(y))

## INT -> STRING
y_str = str(y)
print(y_str, type(y_str))

## INT -> FLOAT
y_float = float(y)
print(y_float, type(y_float))

## INT -> Bool
y_bool = bool(y)
print(y_bool, type(y_bool))